#include <unistd.h>

void ft_first_word(char *str)
{
    while (*str == ' ' || *str == '\t')
        str++;

    while (*str != '\0' && *str != ' ' && *str != '\t')
    {
        write(1, str, 1);
        str++;
    }
    write(1, "\n", 1);
}
