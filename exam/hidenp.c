#include <unistd.h>

void hidenp(char *s1, char *s2)
{
    while (*s2)
    {
        if (*s1 && *s1 == *s2)
            s1++;
        s2++;
    }
    if (!*s1)
        write(1, "1", 1);
    else
        write(1, "0", 1);
    write(1, "\n", 1);
}
